//转换方法
//valueOf()方法返回本身
var colors=["red","white","blue"];
console.log(colors.valueOf());      //[ 'red', 'white', 'blue' ]
//toString方法返回每一项调动toString方法再用,连接的字符串
console.log(colors.toString());     //red,white,blue
//toLocaleString()方法和toString原理一样
console.log(colors.toLocaleString());   //red,white,blue
